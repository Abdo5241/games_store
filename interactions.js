let is_open = true;
const nav_bar = document.getElementById("nav_bar")
const nav_items = document.querySelectorAll("#nav_items a")
const nav_toggle = document.getElementById("nav_toggle")


const toggleNavBar = () => {
    if (is_open === true) {
        is_open = false;
        nav_bar.style.width = "50px"
        nav_items.forEach((a_tag) => {
            var p_tag = a_tag.querySelector('p');
            p_tag.classList.add("hidden")
            a_tag.style.padding = ".5rem .5rem"
        })
    }
    else {
        is_open = true;
        nav_bar.style.width = "200px"
        nav_items.forEach((a_tag) => {
            var p_tag = a_tag.querySelector('p');
            p_tag.classList.remove("hidden")
            a_tag.style.padding = ".5rem .5rem"
        })
    }
}

nav_toggle.addEventListener("click", toggleNavBar)

